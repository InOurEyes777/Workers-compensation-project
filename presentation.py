import streamlit as st
import reveal_slides as rs

def presentation_page():
    st.title("Презентация проекта")

    # Контент презентации в формате Markdown
    # Разделитель --- создает новый слайд
    presentation_markdown = """
# Прогнозирование стоимости страховых выплат
---
## Введение
*   **Цель:** Разработка модели для предсказания итоговой стоимости страхового возмещения.
*   **Актуальность:** Страховые компании нуждаются в точных инструментах для формирования резервов.
*   **Датасет:** Workers Compensation (100 000 записей, OpenML ID: 42876).
---
## Описание данных
*   **Целевая переменная:** `UltimateIncurredClaimCost` (Итоговая стоимость).
*   **Признаки:** Возраст, Пол, Зарплата, Дата случая, Начальная оценка и др.
*   **Проблема:** Данные содержат категориальные переменные и даты, требующие специальной обработки.
---
## Предобработка данных
1.  **Извлечение признаков из дат:** Созданы `AccidentMonth`, `AccidentDayOfWeek`, `ReportingDelay`.
2.  **Кодирование:** Категориальные признаки преобразованы в числа.
3.  **Масштабирование:** Числовые признаки нормализованы (`StandardScaler`), а также трансформированы с помощью логарифмирования.
4.  **Очистка:** Удален признак `ClaimDescription` (текстовые описания создают шум).
---
## Выбор моделей
Для сравнения были обучены 4 модели:
1.  **Linear Regression** (Базовая модель).
2.  **Ridge Regression** (Регуляризация).
3.  **Random Forest** (Ансамблевый метод).
4.  **XGBoost** (Градиентный бустинг).
---
## Результаты обучения
**Ключевой вывод:** Модели показали среднее качество (R²).

*   **Linear Regression / Ridge:** R² = 0.2711$.
*   **Random Forest:** R² = 0.4$.
*   **XGBoost:** R² = 0.43$.
*   **Ridge:** R² = 0.2711$.

**Причина:** Низкая корреляция между доступными признаками и итоговой суммой выплаты.
---
## Анализ важности признаков
*   **Топ-1:** `InitialCaseEstimate` (Начальная оценка).
*   **Топ-2:** `WeeklyPay` (Зарплата).
*   **Топ-3:** `ReportingDelay` (Задержка отчета).

*Вывод:* Начальная оценка является лучшим предиктором, но её точность недостаточна. Зарплата и задержка обращения играют важную роль.
---
## Анализ ошибок
*   Модели склонны **занижать** стоимость крупных выплат (выбросы).
*   Большая концентрация ошибок в диапазоне малых и средних выплат.
*   Это подтверждает гипотезу о высокой волатильности страховых случаев.
---
## Streamlit-приложение
*   Реализован интерактивный интерфейс.
*   Функционал:
    *   Загрузка и предобработка данных.
    *   Обучение моделей "в один клик".
    *   Визуализация метрик и важности признаков.
    *   Предсказание для новых случаев.
---
## Заключение
- Модель может помочь страховым компаниям в планировании
- Возможности улучшения: добавление внешних данных, ансамбли моделей
    """

    # Настройки боковой панели
    with st.sidebar:
        st.header("Настройки презентации")
        theme = st.selectbox("Тема", ["black", "white", "league", "beige", "sky", "night"], index=0)
        height = st.number_input("Высота слайдов", value=500)
        transition = st.selectbox("Переход", ["slide", "convex", "concave", "zoom"], index=0)
        plugins = st.multiselect("Плагины", ["highlight", "notes", "search", "zoom"], default=[])

    # Отрисовка презентации
    rs.slides(
        presentation_markdown,
        height=height,
        theme=theme,
        config={
            "transition": transition,
            "plugins": plugins,
        },
        markdown_props={"data-separator-vertical": "^--$"}, # Разделитель для вертикальных слайдов (если нужно)
    )

# Важно: вызов функции, чтобы Streamlit отобразил контент
if __name__ == "__main__":
    presentation_page()
